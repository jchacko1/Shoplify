    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField13.getText();
        String password = jPasswordField1.getText();
        String firstname = jTextField1.getText();
        String lastname = jTextField2.getText();
        String DOB = jTextField14.getText();
        String gender = jComboBox1.getSelectedItem().toString();
        String address = jTextArea1.getText();
        String email = jTextField10.getText();
        String phone = jTextField11.getText();
        String securityQuestion = jComboBox1.getSelectedItem().toString();
        String securityAnswer = jTextField12.getText();

        SecurityQuestionsModel securityQuestionsModel = new SecurityQuestionsModel();
        int securityQuestionId = securityQuestionsModel.getSecurityId(securityQuestion);



        if (AccountController.createAccountAndUser(username, password, firstname, lastname, DOB, gender, address, email, phone, securityQuestionId, securityAnswer)) {
            signupPromptView frame = new signupPromptView();
            frame.setVisible(true);
            dispose();

        } else {
            signupFailView frame = new signupFailView();
            frame.setVisible(true);
        }

    }//GEN-LAST:event_jButton1ActionPerformed


